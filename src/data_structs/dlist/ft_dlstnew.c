#include "libft.h"

t_dlist	*ft_dlstnew(void *content)
{
	t_dlist	*list;

	list = ft_calloc(1, sizeof(t_list));
	if (!list)
		return (NULL);
	list->content = content;
	list->next = NULL;
	list->prev = NULL;
	return (list);
}
